document.querySelector('html').dataset.theme = localStorage.getItem('roku.ui.theme')
const link = document.createElement('link')
link.setAttribute('id', 'theme-light')
link.setAttribute('href', '/light.css')
link.setAttribute('rel', 'stylesheet')
link.setAttribute('as', 'style')
document.head.appendChild(link)
const dark = document.createElement('link')
dark.setAttribute('id', 'theme-dark')
dark.setAttribute('href', '/dark.css')
dark.setAttribute('rel', 'stylesheet')
dark.setAttribute('as', 'style')
document.head.appendChild(dark)
